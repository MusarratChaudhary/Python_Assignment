# Arithmetic Operators in Python:
# Arithmetic operators are used to perform basic mathematical operations
# like addition, subtraction, multiplication, division, etc.

# ADITION
a = 10
b = 5

print(a + b)

# SUBTRACTION
a = 20
b = 8

print(a - b)

# MULTIPLICATION
a = 5
b = 4

print(a * b)

# DIVISION
# 1- Float Division: Always returns the result in decimal (float), even if both numbers are integers.
a = 10
b = 6

print(a / b)

# 2- # FLOOR/INTEGER DIVISION: Always returns the result as an integer by rounding down 
a = 10
b = 6

print(a // b)

# MODULUS
# find the remainder when one number is divided by another.
a = 15 
b = 4

print(a % b)

# EXPONENTIATION
print(2 ** 3)  # 2 multiplied by itself three times equals 8

