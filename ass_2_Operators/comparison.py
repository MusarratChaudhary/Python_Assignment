# COMPARISON OPERATOR:
# Comparison operators are used to compare two values.
# These always return the result in True or False.

# EQUAL TO
a = 20
b = 20

print(a == b)     # Output: True (Both values are equal)

# NOT EQUAL TO
a = 5
b = 15            
 
print( a != b)     # Output: True (Both values are equal)

# GREATER THAN
a = 20
b = 10

print(a > b)      # Output: True (20 is greater than 10)

# LESS THAN
a = 15
b = 5

print(a < b)      # Output: False (5 is less than 15)

# LESS THAN or EQUAL TO
a = 20
b = 15

print(a <= b)     # Output: False (15 is less than 20 and both values are not equal)

# GREATER THAN or EQUAL TO
a = 15
b = 40

print(a >= b)      # Output: False (40 is greater than 15 and both values are not equal)